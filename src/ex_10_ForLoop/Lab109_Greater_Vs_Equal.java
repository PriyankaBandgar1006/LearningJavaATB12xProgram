package ex_10_ForLoop;

public class Lab109_Greater_Vs_Equal {
    public static void main(String[] args) {
        for (int i=0;i<=10;i++){ // it will run 11 times
            System.out.println(i); // output is from 0 t0 10
        }

        for (int i=1;i<=10;i++){ // it will run 10 times
            System.out.println(i); // output is from 1 to 10
        }

        for (int i=0;i<10;i++){ // it will run 10 times
            System.out.println(i); // output is from 0 to 9
        }
    }

}
